    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // 저장 
       countc=todo_Table.getRowCount();
        
        if(countc>0)
        {
        try{
        File file = new File("data.txt"); // data.txt 파일에 다음과 같은 내용을 저장.
        if(!file.exists()){
            file.createNewFile();
        }
            FileWriter fw = new FileWriter(file.getAbsoluteFile());
            BufferedWriter bw = new BufferedWriter(fw);
            for(int i =0 ; i < todo_Table.getRowCount(); i++){ // 로우의 카운트
                for(int j=0 ; j<todo_Table.getColumnCount(); j++) {
                    bw.write(todo_Table.getModel().getValueAt(i,j)+"/"); // 구별 문자.
                }
                bw.write("\n");
            }
            bw.close();
            fw.close();
            JOptionPane.showMessageDialog(null,"저장 완료");
        }
        catch(Exception ex) 
        {}
        
        }
        else
           JOptionPane.showMessageDialog(null,"테이블에 저장할 내용이 없습니다!"); 
    }//GEN-LAST:event_jMenuItem1ActionPerformed

